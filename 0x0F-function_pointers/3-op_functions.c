#include "3-calc.h"


/*
 * op_add - calculate the add of a and b
 * @a: number 1
 * @b: number 2
 * Return: the add of a and b
 */
int op_add(int a, int b)
{
        return (a + b);
}


/*
 * op_add - calculate the sum of a and b
 * @a: number 1
 * @b: number 2
 * Return: the sum of a and b
 */
int op_sub(int a, int b)
{
        return (a - b);
}

/*
 * op_mul - calculate the mul of a and b
 * @a: number 1
 * @b: number 2
 * Return: the mul of a and b
 */
int op_mul(int a, int b)
{
        return (a * b);
}

/*
 * op_div - calculate the div of a and b
 * @a: number 1
 * @b: number 2
 * Return: the div of a and b
 */
int op_div(int a, int b)
{
        return (a / b);
}

/*
 * op_mod - calculate the mod of a and b
 * @a: number 1
 * @b: number 2
 * Return: the mod of a and b
 */
int op_mod(int a, int b)
{
        return (a % b);
}
